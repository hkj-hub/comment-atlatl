/* eslint-disable no-undef */
import { SkyWayAuthToken, uuidV4, nowInSec } from '@skyway-sdk/token';

const applicationToken = process.env.APPLICATION_TOKEN;
const secretToken = process.env.SECRET_TOKEN;

const token = new SkyWayAuthToken({
  jti: uuidV4(),
  iat: nowInSec(),
  exp: nowInSec() + 60 * 60 * 24,
  scope: {
    app: {
      id: `${applicationToken}`,
      turn: true,
      actions: ['read'],
      channels: [
        {
          id: '*',
          name: '*',
          actions: ['write'],
          members: [
            {
              id: '*',
              name: '*',
              actions: ['write'],
              publication: {
                actions: ['write'],
              },
              subscription: {
                actions: ['write'],
              },
            },
          ],
          sfuBots: [
            {
              actions: ['write'],
              forwardings: [
                {
                  actions: ['write'],
                },
              ],
            },
          ],
        },
      ],
    },
  },
}).encode(`${secretToken}`);
console.log(`${process.env.APPLICATION_URL}/?token=${token}`);
